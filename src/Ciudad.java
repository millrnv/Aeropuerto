package Aeropuerto;

public class Ciudad {

	private string nombreCiudad;
	private string latitud;
	private string longitud;

	public string getNombreCiudad() {
		return this.nombreCiudad;
	}

	public string getLatitud() {
		return this.latitud;
	}

	public string getLongitud() {
		return this.longitud;
	}

}